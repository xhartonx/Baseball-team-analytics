--Create tables
CREATE TABLE [Result](
	gameDate DATE NOT NULL,
	winnerTeam VARCHAR(30),
	loserTeam VARCHAR(30),
	result VARCHAR(4),
	CONSTRAINT pk_Result_gameDate PRIMARY KEY (gameDate))

CREATE TABLE [Schedule](
	gameId CHAR(3) NOT NULL,
	gameOpponent VARCHAR(50),
	gameStadium VARCHAR(50),
	gameDate DATE,
	CONSTRAINT pk_Schedule_gameId PRIMARY KEY (gameId),
	CONSTRAINT fk_Schedule_gameDate FOREIGN KEY (gameDate)
		REFERENCES [Result] (gameDate) 
		ON DELETE CASCADE ON UPDATE CASCADE
	)

CREATE TABLE [Position](
	positionId CHAR(1) NOT NULL,
	playerPosition VARCHAR(20)
	CONSTRAINT pk_Position_positionId PRIMARY KEY (positionId))

CREATE TABLE [Player](
	playerId CHAR(6) NOT NULL,
	playerFirstName VARCHAR(50),
	playerLastName VARCHAR(50),
	playerHeight DECIMAL(2,1),
	playerWeight INT,
	CONSTRAINT pk_Player_playerId PRIMARY KEY (playerId),
	);

CREATE TABLE [Weather](
	weatherDate DATE NOT NULL,
	weatherTemperature INT,
	gameId CHAR(3),
	CONSTRAINT pk_Weather_weatherDate PRIMARY KEY (weatherDate),
	CONSTRAINT fk_Weather_gameId FOREIGN KEY (gameId)
		REFERENCES [Schedule] (gameId) 
		ON DELETE CASCADE ON UPDATE CASCADE
	);


CREATE TABLE [Assign](
	playerId CHAR(6) NOT NULL,
	positionId CHAR(1) NOT NULL
	CONSTRAINT pk_Assign_playerId_positionId PRIMARY KEY (playerId,positionId),
    CONSTRAINT fk_Assign_palyerId FOREIGN KEY (playerId)
		REFERENCES [Player] (playerId)
		ON DELETE CASCADE ON UPDATE CASCADE,
	CONSTRAINT fk_Assign_positionId FOREIGN KEY (positionId)
		REFERENCES [Position] (positionId) 
		ON DELETE CASCADE ON UPDATE CASCADE
	);


CREATE TABLE [Play](
	playerId CHAR(6) NOT NULL,
	gameId CHAR(3) NOT NULL,
	CONSTRAINT pk_Play_playerId_gameId PRIMARY KEY (playerId, gameId),
	CONSTRAINT fk_Play_playerId_gameId FOREIGN KEY (playerId)
		REFERENCES [Player] (playerId)
		ON DELETE CASCADE ON UPDATE CASCADE,
	CONSTRAINT fk_Play_gameId FOREIGN KEY (gameId)
		REFERENCES [Schedule] (gameId)
		ON DELETE CASCADE ON UPDATE CASCADE
	);

