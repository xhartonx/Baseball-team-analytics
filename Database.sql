SELECT Year(r.gameDate) AS 'Year',
	   t.playerPosition,
	   AVG(p.playerWeight) AS 'Average Height of Players',
	   AVG(p.playerHeight) AS 'Average Weight of Players', 
	   r.result
FROM Position t, Player p, Assign a, Play l, Schedule S, Result r
WHERE t.positionId = a.positionId
	AND a.playerId = P.playerId
	AND a.playerId = l.playerId
	AND l.gameId = s.gameId
	AND s.gameDate = r.gameDate
GROUP BY Year(r.gameDate),t.playerPosition, r.result
ORDER BY Year(r.gameDate),t.playerPosition, r.result

