SELECT  a.Month, 
	CAST(a.[Count of Win]*100.0/d.[Count of Result] AS decimal(4,2)) AS 'Winning Rate(%)',
	CAST(b.[Count of Lose]*100.0/d.[Count of Result] AS decimal(4,2)) AS 'Losing Rate(%)',
	COALESCE(CAST(c.[Count of Tie]*100.0/d.[Count of Result] AS decimal(4,2)), 0) AS 'No Result Rate(%)',
	w2.[Average Temperature]
FROM ( SELECT MONTH(r1.gameDate) AS 'Month', COUNT(r1.result) AS 'Count of Win'
FROM result r1
WHERE r1.result = 'W'
GROUP BY MONTH(r1.gameDate)) a 
LEFT JOIN  
(SELECT MONTH(r2.gameDate) AS 'Month', COUNT(r2.result) AS 'Count of Lose'
FROM result r2
WHERE r2.result = 'L'
GROUP BY MONTH(r2.gameDate)) b
ON a.Month = b.Month
LEFT JOIN
(SELECT MONTH(r3.gameDate) AS 'Month', COUNT(r3.result) AS 'Count of Tie'
FROM result r3
WHERE r3.result = 'T'
GROUP BY MONTH(r3.gameDate)) c
ON a.Month = c.Month
LEFT JOIN
(SELECT MONTH(r4.gameDate) AS 'Month', COUNT(r4.result) AS 'Count of Result'
FROM Result r4
GROUP BY MONTH(r4.gameDate)) d
ON a.Month = d.Month

