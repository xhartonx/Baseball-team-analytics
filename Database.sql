--Q1 What are the average weight and height of the baseball team players in each position?
SELECT t.playerPosition AS 'Player Position',
	   CAST(AVG(p.playerHeight) AS decimal(3,2)) AS 'Average Height of Players',
	   AVG(p.playerWeight) AS 'Average Weight of Players'
FROM Player p, Assign a, Position t
WHERE p.playerId = a.playerId
	AND a.positionId = t.positionId 
	AND a.positionId IS NOT NULL
GROUP BY t.playerPosition

